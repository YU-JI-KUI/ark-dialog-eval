def _faq_card(content_html: str, intent_name: str = "", st_q: str = "", bot: str = "") -> str:
    """文本/FAQ/转人工卡:msgContext 是双层转义 JSON,content 是 HTML 富文本。"""
    inner = {
        "msgInfo": {"data": {"content": content_html}},
        "ths_intent_info": {"intent_name": intent_name},
        "stQuestion": st_q,
        "bot_name": bot,
        "source": "zongkong",
    }
    return json.dumps([{
        "roomMark": "person",
        "msgType": "aat_text",
        "msgContext": json.dumps(inner, ensure_ascii=False),
    }], ensure_ascii=False)


